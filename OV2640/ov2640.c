//白平衡设置
//0:自动
//1:太阳sunny
//2,阴天cloudy
//3,办公室office
//4,家里home
void OV2640_Light_Mode(u8 mode)
{
    u8 regccval=0X5E;//Sunny
    u8 regcdval=0X41;
    u8 regceval=0X54;
    switch(mode)
    {
    case 0://auto
        SCCB_WR_Reg(0XFF,0X00);
        SCCB_WR_Reg(0XC7,0X10);//AWB ON
        return;
    case 2://cloudy
        regccval=0X65;
        regcdval=0X41;
        regceval=0X4F;
        break;
    case 3://office
        regccval=0X52;
        regcdval=0X41;
        regceval=0X66;
        break;
    case 4://home
        regccval=0X42;
        regcdval=0X3F;
        regceval=0X71;
        break;
    }
    SCCB_WR_Reg(0XFF,0X00);
    SCCB_WR_Reg(0XC7,0X40);	//AWB OFF
    SCCB_WR_Reg(0XCC,regccval);
    SCCB_WR_Reg(0XCD,regcdval);
    SCCB_WR_Reg(0XCE,regceval);
}
//色度设置
//0:-2
//1:-1
//2,0
//3,+1
